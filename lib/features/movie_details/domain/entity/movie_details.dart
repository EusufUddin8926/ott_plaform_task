class MovieDetails {
  final String title;
  final String year;
  final String genre;
  final String rated;
  final String poster;
  final String plot;
  final String director;
  final String writer;
  final String actors;
  final String language;
  final String country;
  final String awards;
  final String imdbRating;

  MovieDetails({
    required this.title,
    required this.year,
    required this.genre,
    required this.rated,
    required this.poster,
    required this.plot,
    required this.director,
    required this.writer,
    required this.actors,
    required this.language,
    required this.country,
    required this.awards,
    required this.imdbRating,
  });
}
